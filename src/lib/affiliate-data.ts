export function getRandomServices(services: AffiliateService[], count: number): AffiliateService[] {
  const shuffled = [...services].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
}

export function getBalancedJobAgents(): AffiliateService[] {
  const highClass = jobAgentServices.filter(s =>
    s.category === "ハイクラス転職" || s.category === "グローバル転職"
  );
  const specialized = jobAgentServices.filter(s =>
    s.category === "セキュリティ特化" ||
    s.category === "SAP特化" ||
    s.category === "マーケ特化" ||
    s.category === "フリーランス"
  );
  const youngCareer = jobAgentServices.filter(s =>
    s.category === "エンジニア転職" ||
    s.category === "エンジニア就活" ||
    s.category === "安定・技術職" ||
    s.category === "第二新卒"
  );

  return [
    ...getRandomServices(highClass, 2),
    ...getRandomServices(specialized, 2),
    ...getRandomServices(youngCareer, 2)
  ];
}

